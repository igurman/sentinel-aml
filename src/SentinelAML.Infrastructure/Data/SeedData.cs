    private static readonly List<string> AssignedNames =
        ["David Lee", "M. Johnson", "S. Williams", "S. Williams", "Unassigned"];

    private static readonly Random Random = new Random();

    public static async Task InitializeAsync(AppDbContext context) {
        if (!context.Customers.Any()) await SeedCustomers(context);
        if (!context.Transactions.Any()) await SeedTransactions(context);
        if (!context.Alerts.Any()) await SeedAlerts(context);
        if (!context.Tickets.Any()) await SeedTickets(context);
    }

    private static async Task SeedCustomers(AppDbContext context) {
        var customers = new List<Customer>();

        for (var i = 1000; i < 1500; i++) {
            customers.Add(new Customer(
                i,
                Names.ElementAt(i - 1000),
                GetRandomEnumValue<CountryType>(),
                i % 5 == 0,
                GetRandomEnumValue<RiskLevel>(),
                DateOnly.FromDateTime(DateTime.UtcNow)
            ));
        }

        await context.Customers.AddRangeAsync(customers);
        await context.SaveChangesAsync();
    }

    private static async Task SeedTransactions(AppDbContext context) {
        var customers = context.Customers.ToList();

        var seed = new List<Transaction>();

        for (var i = 1000; i < 1500; i++) {
            seed.Add(new Transaction(
                i,
                customers.ElementAt(Random.Next(1, customers.Count)).Id,
                Random.Shared.Next(2, 100) * 100,
                GetRandomEnumValue<CurrencyType>(),
                DateTimeOffset.Now,
                GetRandomEnumValue<CountryType>(),
                GetRandomEnumValue<TransactionDirection>()
            ));
        }

        await context.Transactions.AddRangeAsync(seed);
        await context.SaveChangesAsync();
    }

    private static async Task SeedAlerts(AppDbContext context) {
        var transactions = context.Transactions.ToList();

        var seed = new List<Alert>();

        for (var i = 0; i < transactions.Count; i++) {
            var transaction = transactions[i];
            seed.Add(new Alert(
                1000 + i,
                transaction.CustomerId,
                transaction.Id,
                Random.Next(12, 19) * 5,
                GetListRules(),
                GetRandomEnumValue<AlertStatus>(),
                DateTimeOffset.Now
            ));
        }

        await context.Alerts.AddRangeAsync(seed);
        await context.SaveChangesAsync();
    }

    private static async Task SeedTickets(AppDbContext context) {
        var alerts = context.Alerts.ToList();

        var seed = alerts.Select((alert, i) =>
            new Ticket(
                1000 + i,
                alert.Id,
                Names[Random.Next(0, AssignedNames.Count - 1)],
                GetRandomEnumValue<TicketStatus>(),
                "try to understand this ticket",
                DateTimeOffset.Now)
        ).ToList();

        await context.Tickets.AddRangeAsync(seed);
        await context.SaveChangesAsync();
    }

    private static T GetRandomEnumValue<T>() where T : Enum {
        var values = Enum.GetValues(typeof(T));
        return (T)values.GetValue(Random.Next(values.Length))!;
    }

    private static List<string> GetListRules() {
        var ruleDictionaries = RuleDictionary.GetRules();
        var result = new List<string>();
        var count = Random.Next(2, ruleDictionaries.Count - 1);
        for (var i = 0; i < count; i++) {
            var rule = ruleDictionaries[Random.Next(0, ruleDictionaries.Count - 1)];
            if (result.Contains(rule.Name)) continue;
            {
                result.Add(rule.Name);
            }
        }

        return result;
    }
    
